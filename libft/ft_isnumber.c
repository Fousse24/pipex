#include	"libft.h"

int	ft_isnumber(char *s)
{
	int	i;

	if (s == NULL)
		return (0);
	i = -1;
	if (s[0] == '-' || s[0] == '+')
		i++;
	while (s[++i])
	{
		if (!ft_isdigit(s[i]))
			return (0);
	}
	return (1);
}
